package rts.domain;

public class Subdivision {
	private Integer id_subdivision;
	private String subdivision;
	private Integer id_branch;
	
	public Integer getId_subdivision() {
		return id_subdivision;
	}
	public void setId_subdivision(Integer id_subdivision) {
		this.id_subdivision = id_subdivision;
	}
	public String getSubdivision() {
		return subdivision;
	}
	public void setSubdivision(String subdivision) {
		this.subdivision = subdivision;
	}
	public Integer getId_branch() {
		return id_branch;
	}
	public void setId_branch(Integer id_branch) {
		this.id_branch = id_branch;
	}
	
	
	

}
