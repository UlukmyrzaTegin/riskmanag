package rts.domain;

public class Department {
	private Integer id_department;
	private String department_name;
	private Integer id_subdivision;
	
	public Integer getId_department() {
		return id_department;
	}
	public void setId_department(Integer id_department) {
		this.id_department = id_department;
	}
	public String getDepartment_name() {
		return department_name;
	}
	public void setDepartment_name(String department_name) {
		this.department_name = department_name;
	}
	public Integer getId_subdivision() {
		return id_subdivision;
	}
	public void setId_subdivision(Integer id_subdivision) {
		this.id_subdivision = id_subdivision;
	}
	
	

}
